package com.progm;

import java.util.Scanner;

public class Star_print {

	public static void main(String[] args) {
		
		 for(int i =0 ;i <5 ;i++)
	     {
	    	 for(int j =0;j<i;j++) {
	    		 System.out.print("  *`");
	    		
	    	 }
	    	 System.out.println();
	    	
	    	 
		    
	     }
		 for(int m =0 ;m <5 ;m++)
	     {
	    	 for(int k =0;k<5-m;k--) {
	    		 System.out.print("  *");
	    		
	    	 }
	    	 System.out.println();
	    	
	    	
	     }
		
	     
	}

}
